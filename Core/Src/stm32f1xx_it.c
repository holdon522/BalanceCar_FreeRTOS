/* USER CODE BEGIN 1 */
uint8_t ECHO_IRQ_FLAG = 0;
uint8_t time_cnt = 0;
static int last_distance =0 ;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	int time = 0;
	
	if(GPIO_Pin == GPIO_PIN_1 )
	{
		if(ECHO_IRQ_FLAG == 0)              //上升沿中断
		{ 
			ECHO_IRQ_FLAG = 1;
			
			time_cnt  = 0;
			TIM1->CNT =0;
			HAL_TIM_Base_Start_IT(&htim1);    //启动定时器，开始计时
			
		}
		else                                //下降沿中断
		{
			ECHO_IRQ_FLAG = 0;
			
			HAL_TIM_Base_Stop_IT(&htim1);     //关闭定时器，计时完毕
			
		  time  = (time_cnt*1000 + TIM1->CNT) /2 ;   //统计声音单次传播的时间
			
			Distance = 34000 * time /1000000;		//计算距离
			
			//过滤掉可能出现0这种错误情况
			if(Distance == 0)   Distance = last_distance;
			else                last_distance = Distance;      
			 			
 		}
	}
}

